def generate_tasks() -> List[Task]:
    """100問のタスクを生成する（矛盾なし50問＋矛盾あり50問）"""
    tasks = []
    task_id = 0

    # 各ルールから10問ずつ（consistent 5問 + inconsistent 5問）
    for rule, sentence_sets in SENTENCES.items():
        for sentence in sentence_sets["consistent"]:
            tasks.append(Task(
                task_id=task_id,
                question=sentence,
                label=True,
                world_rule=rule,
            ))
            task_id += 1

        for sentence in sentence_sets["inconsistent"]:
            tasks.append(Task(
                task_id=task_id,
                question=sentence,
                label=False,
                world_rule=rule,
            ))
            task_id += 1

    # シャッフルせず順番通りに返す（再現性のため）
    return tasks


def format_prompt(task: Task) -> str:
    """タスクをプロンプト形式に変換する"""
    return (
        f"ワールドルール：{task.world_rule}\n"
        f"文章：「{task.question}」\n"
        f"この文章はワールドルールと矛盾しますか？\n"
        f"矛盾しない場合は「矛盾しない」、矛盾する場合は「矛盾する」とだけ答えてください。"
    )


